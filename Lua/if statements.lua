local function addition(number1, number2)
    local result = number1 + number2
    
    if result == 4 then
        print("The result is equal to 4")
    elseif result == 6 then
        print("The result is equal to 6")
    else
        print("None of these results add up")
    end
end

addition(2, 2)
addition(3, 3)
addition(6, 6)